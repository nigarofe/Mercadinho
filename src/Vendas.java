    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        try {
            Produto prod = FakeDB.getProdutoComCodigo(Integer.parseInt(jTextField1.getText()));
            if (prod == null) {
                jTextField2.setText("Produto inexistente!");
                jTextField3.setText("Produto inexistente!");
                jTextField4.setText("Produto inexistente!");
            } else {
                jTextField2.setText(prod.getNome());
                jTextField3.setText(String.valueOf(prod.getPreco()));
                jTextField4.setText(String.valueOf(prod.getQuantidade()));
            }

        } catch (NumberFormatException ex) {
            jTextField2.setText("Código inválido!");
            jTextField3.setText("Código inválido!");
            jTextField4.setText("Código inválido!");
        }
    }//GEN-LAST:event_jTextField1KeyReleased


